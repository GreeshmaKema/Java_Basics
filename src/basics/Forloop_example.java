package basics;

public class Forloop_example {

	public static void main(String[] args) {
		for(int i=0;i<100;i++) {
			System.out.println("numbers are:"+i);
		}
		//step1 initialization
		//step2 condition:i<100
		//step3 execute steps if the condition is true then it will execute the steps inside the for loop
		//step4 increment or decrement i++(increment i value by 1)
	}

}
